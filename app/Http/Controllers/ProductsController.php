    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
        $data['products'] = Product::orderBy('id','desc')->paginate(8);
        $data['categories'] = Category::orderBy('id','desc')->get();

